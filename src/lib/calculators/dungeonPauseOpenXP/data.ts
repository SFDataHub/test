/** 1-basige Arrays (Index 0 bleibt 0) */
function oneBased(values: number[], max?: number): number[] {
  const arr = [0, ...values.map((v) => Number(v) || 0)];
  if (max && arr.length < max + 1) while (arr.length < max + 1) arr.push(0);
  return arr;
}

